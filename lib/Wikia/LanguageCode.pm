# Provide the reverse mapping
our %KEYS = map { $CODES{$_} => $_ } keys %CODES;

sub id2key {
	# Handle being called as a class method or a function
	my $id = @_ == 1 ? $_[0] : $_[1];
	return unless $id;

	return $CODES{$id};
}

sub key2id {
	# Handle being called as a class method or a function
	my $key = @_ == 1 ? $_[0] : $_[1];
	return unless $key;

	return $KEYS{$key};
}

